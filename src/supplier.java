    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        String name,address,contact1,mail1,ips1=null;   // difine the variables as string
        name=s_name.getText();
        address=s_address.getText();
        contact1=contact.getText();                    // get the values from frame to above define variables
        mail1=mail.getText();
        ips1=ips.getText();
        
        if(name.isEmpty()) {
           label2.setText("Please enter the supplier name.");     // if name text is empty , give the msg as label
           return ;
        
        }else if (address.isEmpty()) {
           label2.setText("Please enter the supplier address."); // if address text is empty , give the msg as label
           return;
        }else if (contact1.isEmpty()) {
           label2.setText("Please enter the supplier contact number."); // if contact text is empty , give the msg as label
           return;
        }else if (mail1.isEmpty()) {
           label2.setText("Please enter the supplier gmail."); // if gmail text is empty , give the msg as label
           return;
        }else if (ips1.isEmpty()) {
           label2.setText("Please enter the description."); // if discription text is empty , give the msg as label
           return;
        }
        label2.setText("Detail enter Successful.");   //give  a msg for successfull data entry.
        
        
        try{
            String sql = "INSERT INTO supplier"
                    +"(sup_code, sup_name, sup_address, sup_contact, sup_gmail, description)"   // define a string variable and give the sql statement for insert
                    +" VALUES (?,?,?,?,?,?)";
            con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/baby_shop","root","");  //establish a connection with a database
            pst = (com.mysql.jdbc.PreparedStatement) con.clientPrepareStatement(sql);
            pst.setString(1,s_code.getText());
            pst.setString(2,s_name.getText());
            pst.setString(3,s_address.getText());     // set the values to database given from the text in frame
            pst.setString(4,contact.getText());
            pst.setString(5,mail.getText());
            pst.setString(6,ips.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Inserted Successfully");   // give a msgbox for successful data inserting.
            this.setVisible(false);                                        //close this frame
            sup_table spt =new sup_table();
            spt.setVisible(true);                                          //open new frame named as sup_table
            
            
       }
       catch(SQLException | HeadlessException ex){                         // handle the sql errors
           JOptionPane.showMessageDialog(null,ex);
       }
    }//GEN-LAST:event_insertActionPerformed


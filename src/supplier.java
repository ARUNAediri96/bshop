    public void autoIncrement()                                    // create the class for increment, public means that the method is visible and can be called from other objects of other types
    {
       
            try {
                Class.forName("com.mysql.jdbc.Driver");
                
                con=(com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/baby_shop","root","");   // establish a connection with a database
                Statement s=con.createStatement();
                
                rs=s.executeQuery("select Max(sup_code) from supplier ");  // send SQL statements
                rs.next();  //  it shifts the cursor to the next row of the result set from the database and returns true if there is any row, otherwise false
                rs.getString("Max(sup_code)"); // get the maximum value of the sup_code
                
                if(rs.getString("Max(sup_code)")==null)  // if maximum value is null
                    
                {
                     s_code.setText("D0001");  // set the value to s_code
                }
                else{                          //if maximum value is not null
                    
                    Long sup_code=Long.parseLong(rs.getString("Max(sup_code)").substring(2,(rs.getString("Max(sup_code)").length()))); // substring a string to(2,length(sup_code)) & get the maximum value of it
                    sup_code++;     //increment the value of maximum value of above substring variable
                    s_code.setText("D0" +String.format("%03d",sup_code)); // enter the value to s_code , sup_code value get as three place value and add with "D0"
  
                }      
           
          }catch (ClassNotFoundException | SQLException ex) {  //handle the sql errors
                Logger.getLogger(supplier.class.getName()).log(Level.SEVERE, null, ex);
            }
            
    
}
